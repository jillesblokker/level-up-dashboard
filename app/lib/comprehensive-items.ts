// Helper functions
export const getItemById = (id: string): ComprehensiveItem | undefined => {
  return comprehensiveItems.find(item => item.id === id);
};

export const getItemsByType = (type: string): ComprehensiveItem[] => {
  return comprehensiveItems.filter(item => item.type === type);
};

export const getItemsByCategory = (category: string): ComprehensiveItem[] => {
  return comprehensiveItems.filter(item => item.category === category);
};

export const getItemsByRarity = (rarity: string): ComprehensiveItem[] => {
  return comprehensiveItems.filter(item => item.rarity === rarity);
};

export const getEquippableItems = (): ComprehensiveItem[] => {
  return comprehensiveItems.filter(item => item.isEquippable);
};

export const getConsumableItems = (): ComprehensiveItem[] => {
  return comprehensiveItems.filter(item => item.isConsumable);
};

export const getDefaultItems = (): ComprehensiveItem[] => {
  return comprehensiveItems.filter(item => item.isDefault);
};
